package com.ejemplo.introduccion;

public class TiposDatos {
    public static void main(String[] args) {
        // Cadena
        String cadena = "Hello World";

        // Caracter
        char caracter = 'A';

        // Enteros
        long numeroLong = 10000000000000L;
        int numero = 1000000000;
        short numeroShort = 1234;
        byte numeroByte = 127;

        Integer numero2 = null;
        Integer numero3 = 1234;

        // Reales
        float numeroFloat = 123445453774848.45F;
        double numeroDouble = 124545.456656;

        // Booleano
        boolean esMayor = true;
        boolean esMinimo = false;

        // variables por inferencia
        var numeroEntero = 12.4;

        System.out.println("Hola Mundo");
    }
}
